# Operators -> Binary 
print("Arithematic Operator -> Values")

'''
* -> Multiply
/ -> Decimal Division (Decimal Quotient)
// -> Integer Division 
% -> Modulus (Remainder)
+
-
** -> Raise to -> 2 ** 3 = 8
'''

n1 = 11
n2 = 4
print("Multiplication ->",n1,"*",n2,"=",n1*n2)

# Formatted Strings -> fstrings
print(f"Multiplication -> {n1} * {n2} = {n1*n2}")
print(f"Decimal Division -> {n1} / {n2} = {n1/n2}")
print(f"Integer Division -> {n1} // {n2} = {n1//n2}")
print(f"Remainder -> {n1} % {n2} = {n1%n2}")

n1 = 2
n2 = 3
print(f"Raise To -> {n1} ** {n2} = {n1**n2}")

