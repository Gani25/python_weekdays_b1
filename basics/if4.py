# WAP to check whether the number is divisible by 5 or not

# Divisible BY 5 -> divide by 5 and if remainder is 0
num = int(input("Enter a number: "))

if num % 5 == 0:
    print(f"Number = {num} is divisible by 5")
else:
    print(f"Number = {num} is not divisible by 5")

