# Slicing Of List -> Break
numbers = [1,5,-9,6,-10,50,80,5,1,-9,5,3,45]

print(numbers[1:5])
print(numbers[0:])
print(numbers[:])
print(numbers[:5])
print(numbers[::2])
print(numbers[::3])
print(numbers[1::3])

print("Negative Index")
print(numbers[-1])

print(numbers[::-1])
print(numbers[-4:-7:-1])